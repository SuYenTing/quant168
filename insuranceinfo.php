<h3><b>人壽保險：</b></h3>
<h4>人壽保險，簡稱壽險。當被保險人<b>去世</b>或者<b>達到保險合同規定的年齡</b>時，<br>
保險人給付保險金。購買人壽保險能預防被保險人死亡導致其家庭陷入財務困<br>
境，另外購買人壽保險亦能進行財務規劃，防止退休後因收入減少而無法維持<br>
生活開銷。而人壽保險又能分為終身壽險、定期壽險、生死合險三類：</h4>

